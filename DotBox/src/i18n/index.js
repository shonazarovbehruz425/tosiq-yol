function detectLang() {
  try {
    if (typeof window !== 'undefined' && window.__dbLang && DICT[window.__dbLang]) return window.__dbLang;
    const s = JSON.parse(localStorage.getItem('tosiq_yol_settings') || '{}');
    if (s.lang && DICT[s.lang]) return s.lang;
    const l = localStorage.getItem('app_lang');
    if (l && DICT[l]) return l;
  } catch { /* ignore */ }
  return FALLBACK;
}

let currentLang = detectLang();
const subscribers = new Set();

function applyDirection(lang) {
  try {
    const dir = RTL_LANGS.includes(lang) ? 'rtl' : 'ltr';
    document.documentElement.setAttribute('dir', dir);
    document.documentElement.setAttribute('lang', lang);
  } catch { /* ignore */ }
}
applyDirection(currentLang);

export function getLang() {
  return currentLang;
}

export function setLang(lang) {
  if (DICT[lang] && lang !== currentLang) {
    currentLang = lang;
    if (typeof window !== 'undefined') window.__dbLang = lang;
    applyDirection(lang);
    subscribers.forEach(fn => fn(lang));
  }
}

export function t(key, params) {
  const dict = DICT[currentLang] || DICT[FALLBACK];
  let text = dict[key] ?? DICT.en[key] ?? DICT[FALLBACK][key] ?? key;
  if (params) {
    for (const [k, v] of Object.entries(params)) {
      text = text.replace(`{${k}}`, v);
    }
  }
  return text;
}

// React hook: re-renders the component when the language changes.
export function useT() {
  const [, force] = useState(0);
  useEffect(() => {
    const fn = () => force(x => x + 1);
    subscribers.add(fn);
    return () => subscribers.delete(fn);
  }, []);
  return t;
}
